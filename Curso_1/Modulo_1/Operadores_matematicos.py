x = 100
y = 10

print("Valor de x = ", x)
print("Valor de y = ", y)
print("x + y = ", x + y)
print("x - y = ", x - y)
print("x * y = ", x * y)

# Division flotante
x, y = 13, 2
print("x / y = ", x / y)

# Modulo
x, y = 13, 2
print("x % y = ", x % y)

# potencia
x, y = 2, 5
print("x ** y = ", x ** y)

# Division entera
x, y = 13, 2
print("x // y = ", x // y)